CLR_B = '\033[30m'  # Чёрный цвет для пропусков
CLR_W = '\033[37m'  # Белый цвет для рамок
CLR_N = '\033[36m'  # Бирюзовый цвет для чисел
CLR_G = '\033[32m'  # Зелёный цвет для чисел
STL_I = '\033[3m'  # Курсив
STL_B = '\033[1m'  # Жирный шрифт
STL_N = '\033[0m'  # Обычный
MARK = '\033[7m'  # Фон для выделенных ячеек
POWERS = '⁰¹²³⁴⁵⁶⁷⁸⁹'  # Символы степеней
INDEXES = '₀₁₂₃₄₅₆₇₈₉'  # Символы индексов
# Inline-функция для получения строкового представления степени
pwr = lambda n: ''.join(POWERS[int(c)] for c in str(n))
# Inline-функция для получения строкового представления индекса
ind = lambda n: ''.join(INDEXES[int(c)] for c in str(n))
# Inline-функция для выделения цветом чисел
clr_int = lambda x: CLR_N + str(x) + STL_N
# Inline-функция для выделения текста курсивом
ital = lambda s: STL_I + s + STL_N
# Inline-функция для выделения текста жирным шрифтом
bold = lambda s: STL_B + s + STL_N
